//********************************************************************************************
/**
 * @brief		HTTPユーティリティ
 * @author		@loftkun
 */
//********************************************************************************************
var Http = (function() {
	
	//********************************************************************************************
	/**
	 * @brief		コンストラクタ
	*/
	//********************************************************************************************
	var Http = function() {
	};

	var http = Http.prototype;

	//********************************************************************************************
	/**
	 * @brief		GET
	 * @param[in]	url				
	 * @param[in]	param			
	 * @param[in]	onSuccess		
	 * @param[in]	onError			
	 */
	//********************************************************************************************
	http.get = function(url, param, onSuccess, onError) {
		$.ajax(
		{
			type: 'GET',
			cache: false,
			url: url,
			data: param,
			success: onSuccess,
			error: onError,
		});
	};
	
	return Http;
})();
